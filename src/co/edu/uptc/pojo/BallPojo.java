package co.edu.uptc.pojo;
import java.awt.*;

public class BallPojo {
    private Point point;
    private int size;
    private int angle;


    
    public Point getPoint() {
        return point;
    }
    public void setPoint(Point point) {
        this.point = point;
    }
    public int getSize() {
        return size;
    }
    public void setSize(int size) {
        this.size = size;
    }
    public int getAngle() {
        return angle;
    }
    public void setAngle(int angle) {
        this.angle = angle;
    }



}
